import { generateObject } from 'ai';
import { anthropic } from '@ai-sdk/anthropic';
import { z } from 'zod';
import { conciseness } from '@agentuity/evals';
import agent, { type AgentInput, type AgentOutput } from './index';

export const correctnessEval = agent.createEval('code-correctness', {
	description:
		'LLM judges whether the generated code correctly solves the prompt',
	handler: async (ctx, input, output) => {
		const { object: judgment } = await generateObject({
			model: anthropic('claude-haiku-4-5'),
			schema: z.object({
				score: z.number().min(0).max(1),
				passed: z.boolean(),
				reason: z.string(),
			}),
			prompt: `You are evaluating code generated by an AI agent.

The user's prompt was: "${input.prompt}"

The agent generated this code and executed it. Here are the results:

TypeScript result:
- Exit code: ${output.typescript.exitCode}
- Stdout: ${output.typescript.stdout}
- Stderr: ${output.typescript.stderr}

Python result:
- Exit code: ${output.python.exitCode}
- Stdout: ${output.python.stdout}
- Stderr: ${output.python.stderr}

SCORING (0.0-1.0):
- Did both implementations execute successfully (exit code 0)?
- Do the outputs look correct for the given prompt?
- Are the implementations reasonable approaches?

Set passed=true if score >= 0.7.`,
		});

		return {
			passed: judgment.passed,
			score: judgment.score,
			reason: judgment.reason,
		};
	},
});

export const concisenessEval = agent.createEval(
	conciseness<typeof AgentInput, typeof AgentOutput>({
		middleware: {
			transformInput: (input) => ({
				request: input.prompt,
			}),
			transformOutput: (output) => ({
				response: `TypeScript:\n${output.typescript.code}\n\nPython:\n${output.python.code}`,
			}),
		},
	})
);
