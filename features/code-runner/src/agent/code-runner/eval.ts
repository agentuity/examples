import { generateText, Output } from 'ai';
import { openai } from '@ai-sdk/openai';
import { z } from 'zod';
import agent from './index';

const EvalResult = z.object({
	score: z.number().min(0).max(1),
	reason: z.string(),
});

/**
 * Custom Eval (score type): Code Correctness
 * LLM-as-judge: scores whether the generated code correctly solves the prompt.
 */
export const correctnessEval = agent.createEval('code-correctness', {
	description: 'Scores whether the generated code correctly solves the prompt',
	handler: async (ctx, input, output) => {
		try {
			const { output: result } = await generateText({
				model: openai('gpt-5-nano'),
				output: Output.object({ schema: EvalResult }),
				prompt: `Evaluate code generated by an AI agent.

The user's prompt was: "${input.prompt}"

TypeScript code:
\`\`\`typescript
${output.typescript.code}
\`\`\`
- Exit code: ${output.typescript.exitCode}
- Stdout: ${output.typescript.stdout}
- Stderr: ${output.typescript.stderr}

Python code:
\`\`\`python
${output.python.code}
\`\`\`
- Exit code: ${output.python.exitCode}
- Stdout: ${output.python.stdout}
- Stderr: ${output.python.stderr}

Score from 0 (completely wrong) to 1 (correct, clean, idiomatic).
Consider: Did both run successfully? Are outputs correct? Is the code well-structured?`,
			});

			if (!result) {
				ctx.logger.warn('[EVAL] code-correctness: No result from evaluator');
				return { passed: false, score: 0, reason: 'No result from evaluator' };
			}

			const passed = result.score >= 0.7;

			ctx.logger.info('[EVAL] code-correctness: Completed', {
				passed,
				score: result.score,
				reason: result.reason,
			});

			return { passed, score: result.score, reason: result.reason };
		} catch (err) {
			ctx.logger.error('[EVAL] code-correctness: Failed', { error: String(err) });
			return { passed: false, score: 0, reason: `Eval error: ${String(err)}` };
		}
	},
});
